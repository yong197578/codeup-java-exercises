public class StringExercise {
    public static void main(String[] args) {
       String input1 = "We don't need no education";
       String input2 = "We don't need no thought control";
       String input6 = "We don't need no education\nWe don't need no thought control";
       String input3 = "Check \"this\" out!, \"s inside of \"s!";
       String input4 = "In windows, the main drive is usually C:\\";
       String input5 = "I can do backslashes \\, double backslashes \\\\, " +
               "and the amazing triple backslash \\\\\\!\n";
        System.out.println(input1);
        System.out.println(input2);
        System.out.println(input6);
        System.out.println(input3);
        System.out.println(input4);
        System.out.println(input5);
    }
}
